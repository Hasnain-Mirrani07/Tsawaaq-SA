        String errorDescription = prefs.appLanguage == 'en'
            ? "Unexpected error occurred"
            : 'حدث خظأ غير متوقع';

        return CartActionsResponse.makeError(
            error: error, errorMsg: errorDescription);
      }
    }
  }

