import 'dart:io';

import 'package:dio/dio.dart';
import 'package:tasawaaq/app_core/app_core.dart';
import 'package:tasawaaq/features/cart/cart_actions/cart_actions_request.dart';
import 'package:tasawaaq/features/cart/cart_actions/cart_actions_response.dart';

class CartActionsRepo {
  static final prefs = locator<PrefsService>();

  /// getCartList
  static Future<CartActionsResponse> getCartList() async {
    try {
      final Response response = await locator<ApiService>().dioClient.get(
            '${locator<ApiService>().dioClient.options.baseUrl}list/cart',
          );

      return CartActionsResponse.fromJson(response.data);
    } on DioError catch (error) {
      if (error.response?.statusCode == 401 ||
          error.response?.statusCode == 422) {
        return CartActionsResponse.fromJsonError(
            json: error.response?.data, error: error);
      } else if (error.error is SocketException) {
        print('xXx xc ${error.error}');
        return CartActionsResponse.makeError(
            error: error,
            errorMsg: prefs.appLanguage == 'en'
                ? 'No Internet Connection'
                : 'لا يوجد إتصال بالشبكة');
      } else {
        print('xXx xc ${error.error}');
        String errorDescription = prefs.appLanguage == 'en'
            ? "Unexpected error occurred"
            : 'حدث خظأ غير متوقع';

        return CartActionsResponse.makeError(
            error: error, errorMsg: errorDescription);
      }
    }
  }

  /// addToCart
  static Future<CartActionsResponse> addToCart(AddToCartRequest request) async {
